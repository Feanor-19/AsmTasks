int test(char c)
{
    switch (c)
    {
    case 'c':
        return 'c';
        break;
    case 's':
        return 's';
        break;
    case '%':
        return '%';
        break;
    case 'd':
        return 'd';
        break;
    case 'x':
        return 'x';
        break;
    case 'o':
        return 'o';
        break;
    case 'b':
        return 'b';
        break;
    default:
        break;
    }
}